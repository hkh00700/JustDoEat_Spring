package com.hanul.justdoeat.dto;

public class RestaurantDTO {
	public String restaurant;
	public float r_latitude;
	public float r_hardness;
	public String r_address, r_tel, r_menu, r_imgpath;
	
	public String getRestaurant() {
		return restaurant;
	}
	public void setRestaurant(String restaurant) {
		this.restaurant = restaurant;
	}
	public float getR_latitude() {
		return r_latitude;
	}
	public void setR_latitude(float r_latitude) {
		this.r_latitude = r_latitude;
	}
	public float getR_hardness() {
		return r_hardness;
	}
	public void setR_hardness(float r_hardness) {
		this.r_hardness = r_hardness;
	}
	public String getR_address() {
		return r_address;
	}
	public void setR_address(String r_address) {
		this.r_address = r_address;
	}
	public String getR_tel() {
		return r_tel;
	}
	public void setR_tel(String r_tel) {
		this.r_tel = r_tel;
	}
	public String getR_menu() {
		return r_menu;
	}
	public void setR_menu(String r_menu) {
		this.r_menu = r_menu;
	}
	public String getR_imgpath() {
		return r_imgpath;
	}
	public void setR_imgpath(String r_imgpath) {
		this.r_imgpath = r_imgpath;
	}
	
	

}
